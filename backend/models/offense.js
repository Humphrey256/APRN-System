const mongoose = require('mongoose');

const offenseSchema = new mongoose.Schema({
  plateNumber: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model('Offense', offenseSchema);